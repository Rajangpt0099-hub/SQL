-- Q12. Find airlines that operate more than 1 flight.
select a.airline_name,COUNT(f.flight_id) AS Total_flights
from Airlines a
inner join Flights f
on a.airline_id = f.airline_id
group by a.airline_name
having Total_flights > 1;

-- Q13. Find the highest fare paid for each flight.
select flight_id,MAX(fare) AS highest_fare
from Bookings
group by flight_id;

-- Q14. Find flights where the highest fare paid is 10000 or more.
select flight_id,MAX(fare) AS highest_fare
from Bookings
group by flight_id
having highest_fare >= 10000;

-- Q15. Find each airline's average distance covered by its flights, showing
--      only airlines whose average flight distance is more than 1200 km.

select a.airline_name,ROUND(AVG(f.distance_km), 2) AS Avg_distance
from Airlines a
inner join Flights f
on a.airline_id = f.airline_id
group by a.airline_name
having  Avg_distance > 1200;



