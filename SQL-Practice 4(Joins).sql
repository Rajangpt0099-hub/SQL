-- Create a view named SeatClassSummaryView that shows the seat class along with the total number of bookings and the average fare for each seat class.
-- Then write a query to select from this view the seat class with the higher average fare.
Create view SeatClassSummaryView as
select seat_class,
count(booking_id) as Total_booking,
avg(fare) as Average_fare
from Bookings
group by seat_class;

select * from SeatClassSummaryView
ORDER BY average_fare DESC;


