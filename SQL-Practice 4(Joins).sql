-- Q1. List every flight along with the name of the airline operating it.
select f.flight_number,a.airline_name
from Flights f
inner join Airlines a
on f.airline_id = a.airline_id;

-- Q2. Find the total number of bookings for each flight.
select flight_id,COUNT(booking_id) AS Total_bookings
from Bookings
group by flight_id;

-- Q3. Find flights that have more than 1 booking.
select flight_id,COUNT(booking_id) AS Total_bookings
from Bookings
group by flight_id
having COUNT(booking_id) > 1;

-- Q4. Find the total revenue (sum of fare) generated by each flight.
select flight_id,SUM(fare) AS total_revenue
from Bookings
group by flight_id;

-- Q5. Find flights whose total revenue is more than 10000.
select flight_id,SUM(fare) AS total_revenue
from Bookings
group by flight_id
having SUM(fare)>10000;

-- Q6. Find the total revenue generated by each airline (joining all three tables).
select a.airline_name,SUM(b.fare) as Total_revenue
from Airlines a
inner join Flights f
on a.airline_id = f.airline_id
inner join Bookings b
on f.flight_id = b.flight_id
group by a.airline_name;

-- Q7. Find airlines whose total revenue is more than 15000.
select a.airline_name,SUM(b.fare) as Total_revenue
from Airlines a
inner join Flights f
on a.airline_id = f.airline_id
inner join Bookings b
on f.flight_id = b.flight_id
group by a.airline_name
having SUM(b.fare) > 15000;

-- Q8. Find the average fare paid per booking for each airline.
select a.airline_name,round(avg(b.fare),2)as AVG_FARE_PER_BOOKING
from Airlines a
inner join Flights f
on a.airline_id = f.airline_id
inner join Bookings b
on f.flight_id = b.flight_id
group by a.airline_name;

-- Q9. Find airlines where the average fare is greater than 6000.
select a.airline_name,round(avg(b.fare),2)as AVG_FARE_PER_BOOKING
from Airlines a
inner join Flights f
on a.airline_id = f.airline_id
inner join Bookings b
on f.flight_id = b.flight_id
group by a.airline_name
having round(avg(b.fare),2) > 6000;

-- Q10. Find the number of bookings made in each seat class (Economy vs Business).
select seat_class,COUNT(seat_class ) as Total_bookings
